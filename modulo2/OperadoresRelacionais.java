package modulo2;

public class OperadoresRelacionais {
    public static void main(String[] args) {
        // maior que
        boolean isDezMaiorQueVinte = 10 > 20;
        System.out.println(isDezMaiorQueVinte);

        // menor que
        boolean isDezMenorQueVinte = 10 < 20;
        System.out.println(isDezMenorQueVinte);

        // maior ou igual que
        boolean isDezMaiorOuIgualQueVinte = 10 >= 20;
        System.out.println(isDezMaiorOuIgualQueVinte);

        // menor ou igual que
        boolean isDezMenorOuIgualQueVinte = 10 <= 20;
        System.out.println(isDezMenorOuIgualQueVinte);

        // igual a
        boolean isDezIgualADez = 10 == 10;
        System.out.println(isDezIgualADez);

        // diferente de
        boolean isDezDiferenteDeDez = 10 != 10;
        System.out.println(isDezDiferenteDeDez);

    }
}
